import { IGameObject } from "../Interfaces/IGameObject.js";

export class GameObject implements IGameObject {
    x: number;
    y: number;
    width: number;
    height: number;

    constructor(x: number, y: number, width: number, height: number) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}